package org.example;

public class conditional {
    public static void main(String[] args) {
        System.out.println(true?10:20);
        System.out.println(false? 10.4:21.7);
        System.out.println(10>20?"hii":"hello");
        System.out.println(10>20?"hi":12);
        System.out.println('a'<=97?true:false);
        System.out.println("hii"=="hello"?10+12.5:'a'>'b');
    }
}
